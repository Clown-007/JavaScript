// Example

var firstName = "Ada";

var secondLetterOfFirstName = firstName[1];

console.log(secondLetterOfFirstName);


var lastName = "Lovelace";

var thirdLetterOfLastName = lastName[2];
console.log(thirdLetterOfLastName); // This is to print the 3rd word of the Last Name 



//To get The Last Letter of the Name.

var lastLetterOfLastName = lastName[lastName.length -1];
console.log(lastLetterOfLastName);

// If we want to find the second last word or third last we can just do

var secondLastLetterOfLastName = lastName[lastName.length -2];
console.log(secondLastLetterOfLastName);

